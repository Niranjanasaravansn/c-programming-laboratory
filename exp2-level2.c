#include<stdio.h>
int main()

{
  char n;
  scanf("%c",&n);
  switch(n)
  { 
      case'E':
      printf("Excellent");
      break;
      
      case'v':
      printf("very good");
      break;
      
     case'G':
      printf("good");
      break;
      
      case'A':                                                       
      printf("Average");
      break;
      
      default:
      printf("invalid Grade");
      break;
        
}

} 
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        



